// Convert MM/DD/YYYY to YYYY-MM-DD (ISO 8601) for Alloy API
export function toIsoDate(dob) {
  if (!dob) return ''
  const parts = dob.split('/')
  if (parts.length !== 3) return dob
  const [mm, dd, yyyy] = parts
  return `${yyyy}-${mm}-${dd}`
}

